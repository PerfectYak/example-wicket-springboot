package com.example.wicket.vo;

public class ZipVo {
    private String zip;
    private String City;

    public String getZip() {
        return zip;
    }

    public ZipVo setZip(String zip) {
        this.zip = zip;
        return this;
    }

    public String getCity() {
        return City;
    }

    public ZipVo setCity(String city) {
        City = city;
        return this;
    }
}
